def segment_image(image_path):
    """Сегментирует одно изображение и возвращает RGB маску"""
    image = Image.open(image_path).convert("RGB")
    original_size = image.size[::-1]

    inputs = processor(images=image, return_tensors="pt")
    inputs = {k: v.to(device) for k, v in inputs.items()}

    with torch.no_grad():
        outputs = model(**inputs)

    pred_mask = processor.post_process_semantic_segmentation(outputs, target_sizes=[original_size])[0]
    pred_mask_np = pred_mask.cpu().numpy()

    # Цветовая схема для маски
    colors = np.array([
        [0, 0, 0],       # 0: фон
        [0, 255, 0],     # 1: растительность
        [255, 255, 0],   # 2: земля
        [0, 0, 255],     # 3: вода
        [255, 0, 0],     # 4: здания
        [128, 128, 128], # 5: дороги
        [255, 0, 255],   # 6: другое
        [255, 165, 0],   # 7: дополнительный
    ])

    h, w = pred_mask_np.shape
    rgb_mask = np.zeros((h, w, 3), dtype=np.uint8)
    for class_id, color in enumerate(colors):
        if class_id < len(colors):
            rgb_mask[pred_mask_np == class_id] = color

    return rgb_mask

