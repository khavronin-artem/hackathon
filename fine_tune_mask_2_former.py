def show_3x3_mosaic_center(folder_path):
    """
    Собирает мозаику 3x3 вокруг центральной точки с шагом 4
    Сетка:
    (x-4, y-4)  (x, y-4)  (x+4, y-4)
    (x-4, y)    (x, y)    (x+4, y)
    (x-4, y+4)  (x, y+4)  (x+4, y+4)
    """
    # Получаем все PNG файлы
    image_files = [f for f in Path(folder_path).glob("*.png")]

    if len(image_files) == 0:
        print("❌ Нет изображений в папке!")
        return

    # Создаем словарь для быстрого доступа
    tile_dict = {}
    for f in image_files:
        x, y = map(int, f.stem.split('_'))
        tile_dict[(x, y)] = f

    # Получаем все доступные координаты
    all_x = sorted(set(x for x, _ in tile_dict.keys()))
    all_y = sorted(set(y for _, y in tile_dict.keys()))

    # Выбираем случайную центральную точку (не слишком близко к краям)
    min_x = min(all_x) + 4
    max_x = max(all_x) - 4
    min_y = min(all_y) + 4
    max_y = max(all_y) - 4

    if min_x > max_x or min_y > max_y:
        print("❌ Недостаточно места для шага 4!")
        return

    center_x = random.choice([x for x in all_x if min_x <= x <= max_x])
    center_y = random.choice([y for y in all_y if min_y <= y <= max_y])

    # Координаты сетки 3x3
    offsets = [-4, 0, 4]
    grid = []
    for dy in offsets:
        for dx in offsets:
            x = center_x + dx
            y = center_y + dy
            grid.append((x, y))

    print(f"\n📊 Центр: X={center_x}, Y={center_y}")
    print(f"📊 Координаты:")
    for x, y in grid:
        print(f"    X={x}, Y={y} -> {'✅' if (x,y) in tile_dict else '❌'}")

    # Проверяем что все 9 картинок есть
    missing = [c for c in grid if c not in tile_dict]
    if missing:
        print(f"\n❌ Отсутствуют картинки: {missing}")
        return

    # Загружаем размер
    sample = Image.open(tile_dict[grid[0]])
    w, h = sample.size

    # Создаем мозаику
    mosaic = Image.new('RGB', (w * 3, h * 3))

    for idx, (x, y) in enumerate(grid):
        row = idx // 3
        col = idx % 3
        img = Image.open(tile_dict[(x, y)])
        mosaic.paste(img, (col * w, row * h))

    # Показываем
    plt.figure(figsize=(12, 12))
    plt.imshow(mosaic)
    plt.title(f"Мозаика 3x3 с шагом 4\nЦентр: ({center_x}, {center_y})", fontsize=12)
    plt.axis('off')
    plt.show()

