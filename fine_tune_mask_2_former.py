def build_map_with_stride_4(folder_path, show_preview=True, save_path=None):
    """
    Собирает карту с шагом 4 (пропуская каждый второй тайл по X и Y)
    Так как файлы лежат с шагом 2, берем каждый второй файл = шаг 4
    """

    # Получаем все PNG файлы
    image_files = list(Path(folder_path).glob("*.png"))

    if len(image_files) == 0:
        print("❌ Нет изображений в папке!")
        return None

    print(f"📁 Найдено тайлов: {len(image_files)}")

    # Парсим координаты из имен файлов
    tile_dict = {}
    for f in image_files:
        try:
            x, y = map(int, f.stem.split('_'))
            tile_dict[(x, y)] = f
        except:
            print(f"⚠️ Пропущен файл с нестандартным именем: {f.name}")

    # Получаем все уникальные координаты
    all_x = sorted(set(x for x, _ in tile_dict.keys()))
    all_y = sorted(set(y for _, y in tile_dict.keys()))

    print(f"\n📊 Исходные данные:")
    print(f"  Диапазон X: {min(all_x)} → {max(all_x)} (всего {len(all_x)} значений)")
    print(f"  Диапазон Y: {min(all_y)} → {max(all_y)} (всего {len(all_y)} значений)")

    # Проверяем шаг между соседними координатами
    if len(all_x) > 1:
        x_step = all_x[1] - all_x[0]
        print(f"  Шаг по X в файлах: {x_step}")
    if len(all_y) > 1:
        y_step = all_y[1] - all_y[0]
        print(f"  Шаг по Y в файлах: {y_step}")

    # Берем каждый второй элемент (шаг 4 вместо 2)
    selected_x = all_x[::2]  # [0, 2, 4, 6, ...] -> каждый второй
    selected_y = all_y[::2]  # [0, 2, 4, 6, ...] -> каждый второй

    print(f"\n📊 После прореживания (шаг 4):")
    print(f"  Выбрано X: {len(selected_x)} из {len(all_x)}")
    print(f"  Выбрано Y: {len(selected_y)} из {len(all_y)}")
    print(f"  Диапазон выбранных X: {min(selected_x)} → {max(selected_x)}")
    print(f"  Диапазон выбранных Y: {min(selected_y)} → {max(selected_y)}")

    # Проверяем наличие всех нужных тайлов
    missing_tiles = []
    for x in selected_x:
        for y in selected_y:
            if (x, y) not in tile_dict:
                missing_tiles.append((x, y))

    if missing_tiles:
        print(f"\n⚠️ Отсутствует {len(missing_tiles)} тайлов из выбранных")
        if len(missing_tiles) <= 20:
            print("Отсутствующие координаты:", missing_tiles)

    # Загружаем размер одного тайла
    first_x, first_y = selected_x[0], selected_y[0]
    sample_tile = Image.open(tile_dict[(first_x, first_y)])
    tile_w, tile_h = sample_tile.size
    print(f"\n📐 Размер одного тайла: {tile_w}x{tile_h}")

    # Размеры полной карты
    cols = len(selected_x)
    rows = len(selected_y)
    full_w = tile_w * cols
    full_h = tile_h * rows

    print(f"🗺️ Размер итоговой карты: {full_w}x{full_h} пикселей ({cols}x{rows} тайлов)")

    # Проверяем память
    memory_mb = (full_w * full_h * 3) / (1024 * 1024)
    print(f"💾 Примерный размер в памяти: {memory_mb:.1f} МБ")

    # Создаем пустую карту
    print("\n🔨 Собираю карту с шагом 4...")
    full_map = Image.new('RGB', (full_w, full_h))

    # Заполняем карту выбранными тайлами
    for row_idx, y in enumerate(selected_y):
        for col_idx, x in enumerate(selected_x):
            if (x, y) in tile_dict:
                tile = Image.open(tile_dict[(x, y)])
                full_map.paste(tile, (col_idx * tile_w, row_idx * tile_h))
            else:
                # Заполняем черным отсутствующие тайлы
                black_tile = Image.new('RGB', (tile_w, tile_h), color='black')
                full_map.paste(black_tile, (col_idx * tile_w, row_idx * tile_h))

        # Прогресс
        if (row_idx + 1) % 10 == 0:
            print(f"  Прогресс: {row_idx + 1}/{rows} строк обработано")

    print("✅ Карта собрана!")

    # Сохраняем если нужно
    if save_path:
        print(f"\n💾 Сохраняю карту в {save_path}...")
        full_map.save(save_path)
        print("✅ Сохранено!")

    # Показываем превью
    if show_preview:
        max_display_size = 2000
        if full_w > max_display_size or full_h > max_display_size:
            scale = max_display_size / max(full_w, full_h)
            preview_w = int(full_w * scale)
            preview_h = int(full_h * scale)
            preview_map = full_map.resize((preview_w, preview_h), Image.Resampling.LANCZOS)
            print(f"\n👁️ Показываю превью (уменьшено до {preview_w}x{preview_h})...")
        else:
            preview_map = full_map
            print(f"\n👁️ Показываю полную карту...")

        plt.figure(figsize=(15, 15))
        plt.imshow(preview_map)
        plt.title(f"Карта с шагом 4\nРазмер: {full_w}x{full_h} | Тайлов: {cols}x{rows}", fontsize=14)
        plt.axis('off')
        plt.tight_layout()
        plt.show()

    return full_map

def show_selection_pattern(folder_path):
    """
    Визуализирует, какие тайлы выбраны (шаг 4) а какие пропущены
    """
    image_files = list(Path(folder_path).glob("*.png"))

    # Парсим координаты
    tile_dict = {}
    for f in image_files:
        try:
            x, y = map(int, f.stem.split('_'))
            tile_dict[(x, y)] = f
        except:
            pass

    all_x = sorted(set(x for x, _ in tile_dict.keys()))
    all_y = sorted(set(y for _, y in tile_dict.keys()))

    # Создаем матрицу: 2 = выбран (каждый второй), 1 = есть но пропущен, 0 = отсутствует
    grid = np.zeros((len(all_y), len(all_x)))

    for row_idx, y in enumerate(all_y):
        for col_idx, x in enumerate(all_x):
            if (x, y) in tile_dict:
                # Проверяем, выбран ли этот тайл (каждый второй по обеим осям)
                x_selected = (all_x.index(x) % 2 == 0)
                y_selected = (all_y.index(y) % 2 == 0)

                if x_selected and y_selected:
                    grid[row_idx, col_idx] = 2  # Выбран
                else:
                    grid[row_idx, col_idx] = 1  # Есть, но пропущен

    # Визуализация
    plt.figure(figsize=(12, 10))

    # Создаем цветовую карту
    from matplotlib.colors import ListedColormap
    cmap = ListedColormap(['black', 'orange', 'green'])

    plt.imshow(grid, cmap=cmap, interpolation='nearest')
    plt.title("Схема выбора тайлов (шаг 4)\n🟢 Выбраны | 🟠 Пропущены | ⚫ Отсутствуют",
              fontsize=14, pad=20)
    plt.xlabel("Индекс X")
    plt.ylabel("Индекс Y")

    # Легенда
    from matplotlib.patches import Patch
    legend_elements = [
        Patch(facecolor='green', label='Выбраны (каждый 2-й по X и Y)'),
        Patch(facecolor='orange', label='Пропущены (есть в данных)'),
        Patch(facecolor='black', label='Отсутствуют в данных')
    ]
    plt.legend(handles=legend_elements, loc='upper right', bbox_to_anchor=(1.15, 1))

    # Статистика
    selected = np.sum(grid == 2)
    skipped = np.sum(grid == 1)
    missing = np.sum(grid == 0)

    print(f"\n📊 Статистика выборки:")
    print(f"  🟢 Выбрано: {selected} тайлов")
    print(f"  🟠 Пропущено: {skipped} тайлов")
    print(f"  ⚫ Отсутствует: {missing} тайлов")
    print(f"  📦 Всего в данных: {selected + skipped} тайлов")

    plt.tight_layout()
    plt.show()

    return grid

