def merge_tiles(base_dir, n, m):
    """
    Объединяет тайлы в мозаику n x m

    base_dir: /content/all_segmented_results
    n: количество тайлов по горизонтали (X)
    m: количество тайлов по вертикали (Y)
    """
    # Получаем все X папки и сортируем
    x_folders = sorted([f for f in Path(base_dir).glob("*_segment")],
                       key=lambda x: int(x.name.replace("_segment", "")))

    # Берем первые n папок по X
    x_folders = x_folders[:n]

    # Получаем все Y файлы из первой папки и сортируем (по убыванию, т.к. Y растет вверх)
    first_folder = x_folders[0]
    y_files = sorted([f for f in first_folder.glob("*_segmented.png")],
                     key=lambda y: int(y.stem.replace("_segmented", "")),
                     reverse=True)

    # Берем первые m файлов по Y
    y_files = y_files[:m]

    # Загружаем размер тайла
    sample = Image.open(y_files[0])
    w, h = sample.size

    # Создаем мозаику
    mosaic = Image.new('RGB', (w * n, h * m))

    # Заполняем
    for i, x_folder in enumerate(x_folders):
        x = x_folder.name.replace("_segment", "")
        for j, y_file in enumerate(y_files):
            y = y_file.stem.replace("_segmented", "")
            tile_path = x_folder / f"{y}_segmented.png"
            tile = Image.open(tile_path)
            mosaic.paste(tile, (i * w, j * h))

    # Координаты левого нижнего угла (первая X папка, последний Y файл)
    bottom_left_x = x_folders[0].name.replace("_segment", "")
    bottom_left_y = y_files[-1].stem.replace("_segmented", "")

    # Сохраняем
    output_dir = f"/content/merged_{bottom_left_x}"
    os.makedirs(output_dir, exist_ok=True)
    output_path = f"{output_dir}/{bottom_left_y}.png"
    mosaic.save(output_path)

    print(f"✅ Сохранено: {output_path}")
    print(f"   Размер: {n} x {m} тайлов")

