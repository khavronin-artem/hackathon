def mask_to_rgb(mask_np, color_map):
    h, w = mask_np.shape
    rgb_mask = np.zeros((h, w, 3), dtype=np.uint8)
    for class_id, color in enumerate(color_map):
        if class_id < len(color_map):
            rgb_mask[mask_np == class_id] = color
    return rgb_mask

