def merge_all_tiles(base_dir, n, m):
    """
    Проходит по всему прямоугольнику с шагом 1 и создает мозаики n x m

    base_dir: /content/all_segmented_results
    n: количество тайлов по горизонтали (X)
    m: количество тайлов по вертикали (Y)
    """
    # Получаем все X папки и сортируем
    all_x = sorted([int(f.name.replace("_segment", "")) for f in Path(base_dir).glob("*_segment")])

    # Получаем все Y файлы из первой папки
    first_folder = Path(base_dir) / f"{all_x[0]}_segment"
    all_y = sorted([int(f.stem.replace("_segmented", "")) for f in first_folder.glob("*_segmented.png")], reverse=True)

    # Загружаем размер тайла
    sample = Image.open(Path(base_dir) / f"{all_x[0]}_segment" / f"{all_y[0]}_segmented.png")
    w, h = sample.size

    # Проходим по всем возможным позициям левого нижнего угла
    for idx_x, start_x in enumerate(all_x[:-n+1]):
        for idx_y, start_y in enumerate(all_y[:-m+1]):
            # Берем n папок по X начиная с start_x
            x_folders = [f"{x}_segment" for x in all_x[idx_x:idx_x+n]]

            # Берем m файлов по Y начиная с start_y
            y_values = all_y[idx_y:idx_y+m]

            # Создаем мозаику
            mosaic = Image.new('RGB', (w * n, h * m))

            for i, x_folder in enumerate(x_folders):
                for j, y in enumerate(y_values):
                    tile_path = Path(base_dir) / x_folder / f"{y}_segmented.png"
                    tile = Image.open(tile_path)
                    mosaic.paste(tile, (i * w, j * h))

            # Координаты левого нижнего угла
            bottom_left_x = all_x[idx_x]
            bottom_left_y = y_values[-1]

            # Сохраняем
            output_dir = f"/content/merged_{bottom_left_x}"
            os.makedirs(output_dir, exist_ok=True)
            output_path = f"{output_dir}/{bottom_left_y}.png"
            mosaic.save(output_path)

            print(f"✅ {bottom_left_x}_{bottom_left_y} ({idx_x+1}/{len(all_x)-n+1}, {idx_y+1}/{len(all_y)-m+1})")

